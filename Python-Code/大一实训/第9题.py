from turtle import *

penup()
goto(0,-50)
pendown()
begin_fill()
fillcolor("black")
circle(150,180)
circle(75,180)
circle(-75,180)
end_fill()
circle(-150,180)
penup()
goto(0,160)
pendown()
begin_fill()
fillcolor("white")
circle(30,360)
end_fill()
penup()
goto(0,0)
pendown()
begin_fill()
fillcolor("black")
circle(30,360)
end_fill()
done()
